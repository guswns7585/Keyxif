import { copyFileSync, existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, parse, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(scriptDir, "..");
const androidLogoDir = resolve(webRoot, "../android/app/src/main/res/drawable");
const webAssetDir = join(webRoot, "assets");
const generatedAssetMap = join(webRoot, "data/logo-assets.generated.js");

if (!existsSync(androidLogoDir)) {
  throw new Error(`Android drawable directory not found: ${androidLogoDir}`);
}

mkdirSync(webAssetDir, { recursive: true });

const logoFiles = readdirSync(androidLogoDir)
  .filter((name) => /^logo_[a-z0-9_]+\.png$/.test(name))
  .sort((a, b) => a.localeCompare(b));

if (logoFiles.length === 0) {
  throw new Error("No Android logo PNG files were found.");
}

for (const fileName of logoFiles) {
  copyFileSync(join(androidLogoDir, fileName), join(webAssetDir, fileName));
}

const mappings = logoFiles.map((fileName) => {
  const key = parse(fileName).name;
  return `  ${JSON.stringify(key)}: ${JSON.stringify(`assets/${fileName}`)}`;
});
const generated = [
  "// Generated by scripts/sync-android-logos.mjs. Do not edit manually.",
  "Object.assign(window.KEYXIF_ASSETS, {",
  mappings.join(",\n"),
  "});",
  "",
].join("\n");

writeFileSync(generatedAssetMap, generated, "utf8");
console.log(`Synced ${logoFiles.length} Android logos to ${webAssetDir}`);
